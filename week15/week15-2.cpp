/// week15-2.cpp 了解字母對應的值
#include <stdio.h>
int main()
{
    if('A'==65) printf("真的誒! A 就是 65\n");
    printf("A的值是%d 數字65對應%c\n", 'A', 65);
    printf("B的值是%d 數字65對應%c\n", 'B', 66);
    printf("C的值是%d 數字65對應%c\n", 'C', 67);
    printf("Z的值是%d 數字65對應%c\n", 'Z', 90);
    printf("\n");
    printf("a的值是%d 數字65對應%c\n", 'a', 97);
    printf("b的值是%d 數字65對應%c\n", 'b', 98);
    printf("c的值是%d 數字65對應%c\n", 'c', 99);
    printf("z的值是%d 數字65對應%c\n", 'z', 122);

}

